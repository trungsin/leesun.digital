class Portada_ThemeGlobalData
{
	/**************************************************************************/
	
	function __construct()
	{
		
	}
	
	/**************************************************************************/
	
	function setGlobalData($name,$functionCallback,$refresh=false)
	{
		global $portada_globalData;
		
		if(isset($portada_globalData[$name]) && (!$refresh)) return($portada_globalData[$name]);
		
		$portada_globalData[$name]=call_user_func($functionCallback);
		
		return($portada_globalData[$name]);
	}

	/**************************************************************************/
}

