class Portada_ThemeVideo
{
	/**************************************************************************/

	function __construct() 
	{ 
		$this->videoSource=array
		(
			'vimeo'																=>	array(__('Vimeo','portada')),
			'youtube'															=>	array(__('YouTube','portada')),
			'dailymotion'														=>	array(__('Dailymotion.com','portada')),
		);
	}

	/**************************************************************************/
	
	function getVideoSource()
	{
		return($this->videoSource);
	}
	
	/**************************************************************************/
}

